/* LAIR TYPES HEADER */



#ifndef __LAIR_TYPES_H
#define __LAIR_TYPES_H 



#include <stdint.h>


/* Unsigned integers */

typedef uint8_t  u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;



/* Signed integers */

typedef int8_t   s8;
typedef int16_t  s16;
typedef int32_t  s32;
typedef int64_t  s64;


/* Floating point values */

typedef float    f32;         // 32 bits pretty much everywhere ( 4 bytes )
typedef double   f64;         // 64 bits 

















#endif        /* __LAIR_TYPES_H END */
